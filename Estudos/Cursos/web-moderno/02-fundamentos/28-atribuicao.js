// Aprendendo sobre atribuições com operações aritméticas.

const a = 7; // Constante 'a' com o valor 7.
let b = 3;   // Variável 'b' com o valor 3.

// Atribuição aditiva (b = b + a):
b += a;
console.log(b); // Resultado: 10 (3 + 7).

// Atribuição subtrativa (b = b - 4):
b -= 4;
console.log(b); // Resultado: 6 (10 - 4).

// Atribuição multiplicativa (b = b * 2):
b *= 2;
console.log(b); // Resultado: 12 (6 * 2).

// Atribuição divisiva (b = b / 2):
b /= 2;
console.log(b); // Resultado: 6 (12 / 2).

// Atribuição modular (b = b % 2):
b %= 2;
console.log(b); // Resultado: 0 (resto de 6 / 2).